package Televizor;

public class Televizor {
    private int canal;
    private int volum;
    private String pornitOprit;

    public Televizor(int volum) {
        this.canal = canal;
        this.volum = volum;
        this.pornitOprit = pornitOprit;
    }

    public int getCanal() {
        return canal;
    }

    public void setCanal(int canal) {
        this.canal = canal;
    }

    public int getVolum() {
        return volum;
    }

    public void setVolum(int volum) {
        this.volum = volum;
    }

    public String getPornitOprit() {
        return pornitOprit;
    }

    public void setPornitOprit(String pornitOprit) {
        this.pornitOprit = pornitOprit;
    }
}
