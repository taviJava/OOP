package PersonalulDinFacultate;

public class MyDate {
    private int an;
    private int luna;
    private int zi;

    public MyDate(int an, int luna, int zi) {
        this.an = an;
        this.luna = luna;
        this.zi = zi;
    }

    public int getAn() {
        return an;
    }

    public void setAn(int an) {
        this.an = an;
    }

    public int getLuna() {
        return luna;
    }

    public void setLuna(int luna) {
        this.luna = luna;
    }

    public int getZi() {
        return zi;
    }

    public void setZi(int zi) {
        this.zi = zi;
    }

}
